//**RANDOM */ Obtener un número aleatorio entre 0 (inclusive) y 1 (exclusive)
const numeroAleatorio = Math.random();

//**FLOOR */ Redondear hacia abajo
const redondeoAbajo = Math.floor(5.9); // Resultado: 5

//**CEIL */ Redondear hacia arriba
const redondeoArriba = Math.ceil(5.1); // Resultado: 6

//**ROUND */ Redondear al entero más cercano
const redondeoCercano = Math.round(5.4); // Resultado: 5

//**ABS */ Obtener el valor absoluto
const valorAbsoluto = Math.abs(-10); // Resultado: 10

//**MAX */ Encontrar el máximo de dos números
const maximo = Math.max(10, 20); // Resultado: 20

//**MIN */ Encontrar el mínimo de dos números
const minimo = Math.min(10, 20); // Resultado: 10

//**POW */ Elevar un número a una potencia
const potencia = Math.pow(2, 3); // Resultado: 8

//**SQRT */ Obtener la raíz cuadrada
const raizCuadrada = Math.sqrt(25); // Resultado: 5

//**SIN */ Obtener el seno de un ángulo en radianes
const seno = Math.sin(Math.PI / 2); // Resultado: 1

//**COS */ Obtener el coseno de un ángulo en radianes
const coseno = Math.cos(Math.PI); // Resultado: -1

//**PI */ Obtener el valor de PI
const pi = Math.PI;

